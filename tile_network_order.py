                for f in tile_layer_used.getFeatures():
                    
                    if fa_to == "Out":
                       tileorder = fa_id                        
                                                  
                    elif fa_to == tileorder:       
                         tileorder += 1         #tileorder = fa_id  
        
                    else:
                        tileorder = fa_id
                    
                    f['Bury_ID'] = tileorder
                    tile_layer_used.updateFeature(f)                          
            

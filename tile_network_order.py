            # # # Reference Layer in ComboBox 1
            index = self.dlg.mMapLayerComboBox.currentIndex()
            selection = self.dlg.mMapLayerComboBox.itemData(index)     # DEM layer
            
            # # # Reference Layer in ComboBox 2
